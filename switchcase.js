let vowel = 'A'
switch (vowel) {
    case 'a':
    case 'A':
        console.log("vowel is a")
        break;
    case 'e':
    case 'E':
        console.log("vowel is e")
        break;
    case 'i':
    case 'I':
        console.log("vowel is i")
        break;
    case 'o':
    case 'O':
        console.log("vowel is o")
        break;
    case 'u':
    case 'U':
        console.log("vowel is u")
        break;
    default:
        console.log("input is not a vowel")
}
